package com.vernalis.pdbconnector.nodes.smilesquery;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;

/**
 * <code>NodeFactory</code> for the "PdbSmilesQuery" Node.
 * Node to perfrom SMILES string queries using the PDB  smilesQuery webservice
 *
 * @author S.Roughley
 */
public class PdbSmilesQueryNodeFactory 
        extends NodeFactory<PdbSmilesQueryNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public PdbSmilesQueryNodeModel createNodeModel() {
        return new PdbSmilesQueryNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<PdbSmilesQueryNodeModel> createNodeView(final int viewIndex,
            final PdbSmilesQueryNodeModel nodeModel) {
        return null;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new PdbSmilesQueryNodeDialog();
    }

}

